/*
  Operadores Aritméticos
    Operadores aritméticos são usados ​​para fazer cálculos matemáticos.
  
  Operadores:
  + adição - Soma de Valores
  - subtração - Subtração de Valores
  * multiplicação - Multiplicação de Valores
  / divisão - Divisão de Valores
  % módulo - Resto da Divisão de Valores
  ** exponenciação - Potência de Valores (ES2016)
  ++ incremento - Incrementa o Valor em 1 (pré-incremento e pós-incremento)
  -- decremento - Decrementa o Valor em 1 (pré-decremento e pós-decremento)
    - pre - incremento e decremento - incrementa ou decrementa antes de retornar o valor
    - pos - incremento e decremento - incrementa ou decrementa depois de retornar o valor
*/

// + adição - Soma de Valores
console.log(2 + 2); /* 4 */

// - subtração - Subtração de Valores
console.log(2 - 2); /* 0 */

// * multiplicação - Multiplicação de Valores
console.log(2 * 2); /* 4 */

// / divisão - Divisão de Valores
console.log(2 / 2); /* 1 */

// % módulo - Resto da Divisão de Valores
console.log(2 % 2); /* 0 */

// ** exponenciação - Potência de Valores (ES2016)
console.log(2 ** 2); /* 4 */

